#include "libft.h"

char*		ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*i;

	i = s1 + ft_strlen(s1);
	while (*s2 != '\0' && n--)
	{
		*i = *s2;
		i++;
		s2++;
	}
	*i = '\0';
	return (s1);
}
